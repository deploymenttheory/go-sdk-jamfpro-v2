type registeredResponse struct {
	statusCode int
	rawBody    []byte
}

// SelfServiceBrandingUploadMock is a test double implementing interfaces.HTTPClient.
type SelfServiceBrandingUploadMock struct {
	responses map[string]registeredResponse
	logger    *zap.Logger
}

func NewSelfServiceBrandingUploadMock() *SelfServiceBrandingUploadMock {
	return &SelfServiceBrandingUploadMock{responses: make(map[string]registeredResponse), logger: zap.NewNop()}
}

func (m *SelfServiceBrandingUploadMock) register(method, path string, statusCode int, fixture string) {
	var body []byte
	if fixture != "" {
		body, _ = os.ReadFile(filepath.Join(mustMocksDir(), fixture))
	}
	m.responses[method+":"+path] = registeredResponse{statusCode: statusCode, rawBody: body}
}

func (m *SelfServiceBrandingUploadMock) RegisterUploadMock() {
	m.register("POST", "/api/self-service/branding/images", 200, "validate_upload.json")
}

func (m *SelfServiceBrandingUploadMock) dispatch(method, path string, result any) (*resty.Response, error) {
	r, ok := m.responses[method+":"+path]
	if !ok {
		return nil, fmt.Errorf("SelfServiceBrandingUploadMock: no response for %s %s", method, path)
	}
	headers := http.Header{"Content-Type": {"application/json"}}
	resp := shared.NewMockResponse(r.statusCode, headers, r.rawBody)
	if result != nil && len(r.rawBody) > 0 {
		_ = json.Unmarshal(r.rawBody, result)
	}
	return resp, nil
}

func mustMocksDir() string {
	_, filename, _, _ := runtime.Caller(0)
	return filepath.Dir(filename)
}

