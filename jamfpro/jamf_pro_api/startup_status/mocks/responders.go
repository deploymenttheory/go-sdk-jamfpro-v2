type registeredResponse struct {
	statusCode int
	rawBody    []byte
}

// StartupStatusMock is a test double implementing transport.HTTPClient.
type StartupStatusMock struct {
	responses map[string]registeredResponse
	logger    *zap.Logger
}

// NewStartupStatusMock returns an empty mock ready for response registration.
func NewStartupStatusMock() *StartupStatusMock {
	return &StartupStatusMock{responses: make(map[string]registeredResponse), logger: zap.NewNop()}
}

func (m *StartupStatusMock) register(method, path string, statusCode int, fixture string) {
	body, _ := os.ReadFile(filepath.Join(mustGetwd(), "mocks", fixture))
	m.responses[method+":"+path] = registeredResponse{statusCode: statusCode, rawBody: body}
}

// RegisterGetStartupStatusMock registers a successful GET /api/startup-status response.
func (m *StartupStatusMock) RegisterGetStartupStatusMock() {
	m.register("GET", "/api/startup-status", 200, "validate_get.json")
}

func (m *StartupStatusMock) dispatch(method, path string, result any) (*resty.Response, error) {
	r, ok := m.responses[method+":"+path]
	if !ok {
		return nil, fmt.Errorf("StartupStatusMock: no response for %s %s", method, path)
	}
	headers := http.Header{"Content-Type": {"application/json"}}
	resp := shared.NewMockResponse(r.statusCode, headers, r.rawBody)
	if result != nil && len(r.rawBody) > 0 {
		_ = json.Unmarshal(r.rawBody, result)
	}
	return resp, nil
}

func mustGetwd() string {
	dir, _ := os.Getwd()
	return dir
}

