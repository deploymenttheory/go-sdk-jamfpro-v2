type registeredResponse struct {
	statusCode int
	rawBody    []byte
}

type PolicyPropertiesMock struct {
	responses map[string]registeredResponse
	logger    *zap.Logger
}

func NewPolicyPropertiesMock() *PolicyPropertiesMock {
	return &PolicyPropertiesMock{responses: make(map[string]registeredResponse), logger: zap.NewNop()}
}

func (m *PolicyPropertiesMock) register(method, path string, statusCode int, fixture string) {
	var body []byte
	if fixture != "" {
		body, _ = os.ReadFile(filepath.Join(mustMocksDir(), fixture))
	}
	m.responses[method+":"+path] = registeredResponse{statusCode: statusCode, rawBody: body}
}

func (m *PolicyPropertiesMock) RegisterGetMock() {
	m.register("GET", "/api/v1/policy-properties", 200, "validate_get.json")
}

func (m *PolicyPropertiesMock) RegisterUpdateMock() {
	m.register("PUT", "/api/v1/policy-properties", 200, "validate_get.json")
}

func (m *PolicyPropertiesMock) dispatch(method, path string, result any) (*resty.Response, error) {
	r, ok := m.responses[method+":"+path]
	if !ok {
		return nil, fmt.Errorf("no mock registered for %s %s", method, path)
	}
	headers := http.Header{"Content-Type": {"application/json"}}
	resp := shared.NewMockResponse(r.statusCode, headers, r.rawBody)
	if result != nil && len(r.rawBody) > 0 {
		_ = json.Unmarshal(r.rawBody, result)
	}
	return resp, nil
}

func mustMocksDir() string {
	_, filename, _, _ := runtime.Caller(0)
	return filepath.Dir(filename)
}

